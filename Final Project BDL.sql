CREATE TABLE transaksi (
    kode_transaksi VARCHAR(10),
    id_buku VARCHAR(10),
    judul VARCHAR(100),
    genre VARCHAR (100),
    jumlah VARCHAR(10),
    harga VARCHAR(100),
    total_harga VARCHAR(100),
    metode_pembayaran VARCHAR(100),
    tanggal VARCHAR(10),
    bulan VARCHAR(10),
    tahun VARCHAR(10),
    nama_kasir VARCHAR(100)
);